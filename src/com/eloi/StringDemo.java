package com.eloi;


public class StringDemo {
    public static void main (String[] args){
        // String

        String name = "JohnDoe";
        System.out.println(name.toUpperCase());
        System.out.println(name.toLowerCase());
        System.out.println(name.charAt(0));

        String doe = "Doe";
        System.out.println(name.contains(doe));

        System.out.println(name.toUpperCase());
    }
}
