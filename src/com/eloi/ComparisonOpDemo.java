package com.eloi;

public class ComparisonOpDemo {
    public static void main (String[] args){
        // Comparison Operators

        int khalidAge = 20;
        int mariamAge = 20;

        System.out.println(khalidAge > mariamAge);
        System.out.println(khalidAge >= mariamAge);
        System.out.println(khalidAge < mariamAge);
        System.out.println(khalidAge <= mariamAge);
        System.out.println(khalidAge == mariamAge);
        System.out.println(khalidAge != mariamAge);


    }
}
